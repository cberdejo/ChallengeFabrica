package fabrica;

public class Cuadro {
    private String fresadoraVertical;
    private String fresadoraHorizontal;
    private String fresadoraDiagonalDerecha;
    private String fresadoraDiagonalIzquierda;

    private String lijaNorte;
    private String lijaSur;
    private String lijaEste;
    private String lijaOeste;

    private String taladradora;


    public String getFresadoraVertical() {
        return fresadoraVertical;
    }

    public void setFresadoraVertical(String fresadoraVertical) {
        this.fresadoraVertical = fresadoraVertical;
    }

    public String getFresadoraHorizontal() {
        return fresadoraHorizontal;
    }

    public void setFresadoraHorizontal(String fresadoraHorizontal) {
        this.fresadoraHorizontal = fresadoraHorizontal;
    }

    public String getFresadoraDiagonalDerecha() {
        return fresadoraDiagonalDerecha;
    }

    public void setFresadoraDiagonalDerecha(String fresadoraDiagonalDerecha) {
        this.fresadoraDiagonalDerecha = fresadoraDiagonalDerecha;
    }

    public String getFresadoraDiagonalIzquierda() {
        return fresadoraDiagonalIzquierda;
    }

    public void setFresadoraDiagonalIzquierda(String fresadoraDiagonalIzquierda) {
        this.fresadoraDiagonalIzquierda = fresadoraDiagonalIzquierda;
    }

    public String getLijaNorte() {
        return lijaNorte;
    }

    public void setLijaNorte(String lijaNorte) {
        this.lijaNorte = lijaNorte;
    }

    public String getLijaSur() {
        return lijaSur;
    }

    public void setLijaSur(String lijaSur) {
        this.lijaSur = lijaSur;
    }

    public String getLijaEste() {
        return lijaEste;
    }

    public void setLijaEste(String lijaEste) {
        this.lijaEste = lijaEste;
    }

    public String getLijaOeste() {
        return lijaOeste;
    }

    public void setLijaOeste(String lijaOeste) {
        this.lijaOeste = lijaOeste;
    }

    public String getTaladradora() {
        return taladradora;
    }

    public void setTaladradora(String taladradora) {
        this.taladradora = taladradora;
    }
}
